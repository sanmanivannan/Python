
num1 = float(input("enter num1: "))
num2 = float(input("enter num2: "))
op = input("enter the operation: ")

if op == "+":
    print(num1+num2)
elif op == "-":
    print(num1-num2)
elif op == "*":
    print(num1*num2)
elif op == "/":
    print(num1/num2)
else:
    print("invalid entry/operation")



