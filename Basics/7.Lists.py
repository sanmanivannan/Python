# simple list

friends = ["Name1","Name2","Name3","Name4","Name5","Name6"]
print(friends)

# index position
print(friends[1])


# index position in the reverse order
print(friends[-3])

# index position there after
print(friends[1:])

# updating the value a  index position 2
friends[2] = "Mani"
print(friends[2])